export class WordDictionary {
  constructor() {
    this.words = wordList;
    this._queues = {};
  }

  _shuffle(arr) {
    const a = [...arr];
    for (let i = a.length - 1; i > 0; i--) {
      const j = Math.floor(Math.random() * (i + 1));
      [a[i], a[j]] = [a[j], a[i]];
    }
    return a;
  }

  _drawFromQueue(tier) {
    if (!this._queues[tier] || this._queues[tier].length === 0) {
      this._queues[tier] = this._shuffle(this.words[tier] || this.words.easy);
    }
    return this._queues[tier].pop();
  }

  getRandomWord(difficulty = 'easy') {
    return this._drawFromQueue(difficulty);
  }

  getWordForDifficulty(difficulty) {
    let tier = 'easy';
    if (difficulty === 'normal') tier = 'medium';
    if (difficulty === 'hard') tier = 'hard';
    if (difficulty === 'hell') tier = 'epic';
    return this._drawFromQueue(tier);
  }

  getBossWord() {
    const word1 = this._drawFromQueue('epic');
    const word2 = this._drawFromQueue('hard');
    return `${word1}-${word2}`;
  }

  getRandomParagraph() {
    const list = this.words.paragraphs;
    return list[Math.floor(Math.random() * list.length)];
  }

  getScribeWord() {
    return this._drawFromQueue('scribe');
  }

  _buildScribePool() {
    // Mix of easy + medium for a balanced timed mode experience
    return this._shuffle([...this.words.easy, ...this.words.medium]);
  }
}

// Patch: ensure scribe queue uses the mixed pool
const _origDraw = WordDictionary.prototype._drawFromQueue;
WordDictionary.prototype._drawFromQueue = function (tier) {
  if (tier === 'scribe') {
    if (!this._queues['scribe'] || this._queues['scribe'].length === 0) {
      this._queues['scribe'] = this._buildScribePool();
    }
    return this._queues['scribe'].pop();
  }
  return _origDraw.call(this, tier);
};
